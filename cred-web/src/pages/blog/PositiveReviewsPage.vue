      A five-star review lands. You smile, move on, and get back to service. Reasonable — except that
      <strong>89% of consumers read businesses' responses to reviews</strong> (BrightLocal, 2024).
      That includes the glowing ones. When you stay silent on a positive review, nearly nine out of ten
      readers see a business that couldn't be bothered to say thank you.
    </p>

    <div class="stat-block">
      <strong>88%</strong> of consumers would use a business that responds to both positive and negative reviews.
